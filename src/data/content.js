export function getGalleryGroups() {
  var groups = {};
  for (var i = 0; i < galleryItems.length; i++) {
    var item = galleryItems[i];
    if (!groups[item.category]) groups[item.category] = [];
    groups[item.category].push(item);
  }
  return groups;
}
